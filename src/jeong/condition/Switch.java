package jeong.condition;

public class Switch {

	public static void main(String[] args) {
		switch(50) {
		case 1 : 
			System.out.println("true");
			break;
		case 2 : 
			System.out.println("false");
			break;
		case 3 : 
			System.out.println("Fuck");
			break;
		default :
			System.out.println("this is default");
			break;
		}
	}

}
